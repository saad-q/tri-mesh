function [C, r] = CCentre(P,Q,R)

D = 2*(P(1)*(Q(2)-R(2)) + Q(1)*(R(2)-P(2)) + R(1)*(P(2)-Q(2)));

u1 = (P(1)^2+P(2)^2)*(Q(2)-R(2)) + (Q(1)^2+Q(2)^2)*(R(2)-P(2)) + (R(1)^2+R(2)^2)*(P(2)-Q(2));
u2 = (P(1)^2+P(2)^2)*(R(1)-Q(1)) + (Q(1)^2+Q(2)^2)*(P(1)-R(1)) + (R(1)^2+R(2)^2)*(Q(1)-P(1));
C = [u1 u2]/D;

r = norm(C - P);

end